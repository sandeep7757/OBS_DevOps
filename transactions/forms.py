class UserLoanCreation(forms.Form):
    full_Name = forms.CharField(widget=forms.TextInput(attrs={'autofocus':'on'}))
    email = forms.CharField(widget=forms.EmailInput(attrs={'autofocus':'off'}))
    birth_date = forms.DateField(widget=forms.DateInput(attrs={'type':'date'}))
    loan_amount = forms.CharField(widget=forms.TextInput(attrs={'autofocus':'off'}))
    loan_purpose = forms.ChoiceField(choices=LOAN_PURPOSE)
    loan_emi = forms.ChoiceField(choices=LOAN_EMI)

    class Meta:
        model = Loan
        fields = [
            'first_name',
            'email',
            'birth_date',
            'loan_amount',
            'loan_purpose',
            'loan_emi',
        ]


    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)

        for field in self.fields:
            self.fields[field].widget.attrs.update({
                'class': (
                    'appearance-none block w-full bg-gray-200 '
                    'text-gray-700 border border-gray-200 rounded '
                    'py-3 px-4 leading-tight focus:outline-none '
                    'focus:bg-white focus:border-gray-500'
                )
            })